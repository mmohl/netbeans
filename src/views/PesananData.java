    private void bUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUpdateActionPerformed
        // TODO add your handling code here:
        Pesanan pesanan = record.get(row);
        pesanan.setLama(tfLama.getText());
        pesanan.setUangMuka(tfUangMuka.getText());
        pesanan.setPembayaran(pembayaranGrup.getSelection().getActionCommand());
        
        try {
            controller.Update(pesanan);
            JOptionPane.showMessageDialog(rootPane, Status.SUCCESS_UPDATE);
            bersih();
        } catch (SQLException ex) {
            Logger.getLogger(PesananData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bUpdateActionPerformed

    private void bDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteActionPerformed
        // TODO add your handling code here:
        Pesanan pesanan = record.get(row);
        
        try {
            int i = JOptionPane.showConfirmDialog(rootPane, Status.MESSAGE_DELETE, Status.TITLE_DELETE, JOptionPane.YES_NO_CANCEL_OPTION);
            if (i == JOptionPane.YES_OPTION) {
                controller.Delete(pesanan.getId());
                JOptionPane.showMessageDialog(rootPane, Status.SUCCESS_DELETE);
                bersih();
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(PesananData.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bDeleteActionPerformed

