    @Override
    public void fillTable() {
        Object object[][] = new Object[record.size()][4];
        int x = 0;
        for(Pesanan obj:record) {
            object[x][0] = obj.getNo_pesanan();
            object[x][1] = obj.getTgl();
            object[x][2] = obj.getPembayaran();
            object[x][3] = obj.getUangMuka();
            x++;
        }
        String judul[] = {"Nomor Pesanan", "Tanggal", "Pembayaran", "Uang Muka"};
        jTable1.setModel(new DefaultTableModel(object, judul));
        jScrollPane1.setViewportView(jTable1);
    }

    @Override
    public void setToTextField() {
        Pesanan pesanan = record.get(row);
        
        tfNama.setText(pesanan.getPenyewa());
        tfKtp.setText(pesanan.getKtp());
        tfNohape.setText(pesanan.getNohape());
        
        switch(pesanan.getPeJeKe()) {
            case "p" :
                tfPeJeKe.setText("Pria");
                break;
            case "w" :
                tfPeJeKe.setText("Wanita");
                break;
        }
        
        taAlamat.setText(pesanan.getAlamat());
        
        // Data Order
        tfLama.setText(pesanan.getLama());
        tfTanggal.setText(pesanan.getTgl());
        tfUangMuka.setText(pesanan.getUangMuka());
        switch(pesanan.getPembayaran()) {
            case "0":
                rbDebit.setSelected(true);
                break;
            case "1":
                rbKredit.setSelected(true);
                break;
        }
        
        // Data Couple
         tfCoupleName.setText(pesanan.getNama());
    }

